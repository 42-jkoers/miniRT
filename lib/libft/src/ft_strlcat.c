#include "libft.h"
#include <stddef.h>

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	dstlen;

	dstlen = ft_strlen(dst);
	if (size < dstlen)
		return (size + ft_strlen((char *)src));
	return (dstlen + ft_strlcpy(dst + dstlen, (char *)src, size - dstlen));
}
